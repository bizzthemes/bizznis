( function( $ ) {
	var api = wp.customize;

	/**
	 * Visibility toggling for some controls
	 */
	$.each({
		'bizznis-settings[nav_extras_enable]': {
			controls: [ 'bizznis_nav_extras' ],
			callback: function( to ) { return !! to; }
		},
		'bizznis-settings[nav_extras]': {
			controls: [ 'bizznis_nav_extras_twitter_id', 'bizznis_nav_extras_twitter_text' ],
			callback: function( to ) { return 'twitter' === to; }
		},
		'bizznis-settings[content_archive]': {
			controls: [ 'bizznis_content_archive_limit' ],
			callback: function( to ) { return 'full' === to; }
		},
		'bizznis-settings[content_archive_thumbnail]': {
			controls: [ 'bizznis_image_size', 'bizznis_image_alignment' ],
			callback: function( to ) { return !! to; }
		},
		'bizznis-settings[blog_title]': {
			controls: [ 'blogname', 'bizznis_hide_site_title', 'blogdescription', 'bizznis_hide_tagline' ],
			callback: function( to ) { return 'text' === to; }
		},
	}, function( settingId, o ) {
		api( settingId, function( setting ) {
			$.each( o.controls, function( i, controlId ) {
				api.control( controlId, function( control ) {
					var visibility = function( to ) {
						control.container.toggle( o.callback( to ) );
					};

					visibility( setting.get() );
					setting.bind( visibility );
				});
			});
		});
	});
	
} )( jQuery );
